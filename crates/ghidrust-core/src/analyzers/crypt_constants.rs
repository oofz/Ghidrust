/// Scan all memory blocks for cryptographic constant tables.
pub fn scan_crypt_constants(prog: &Program) -> Vec<CryptConstantHit> {
    let mut hits = Vec::new();
    for block in &prog.blocks {
        let data = &block.bytes;
        for pat in patterns() {
            if pat.bytes.len() < 4 {
                continue;
            }
            let mut start = 0usize;
            while let Some(rel) = find_subslice(&data[start..], pat.bytes) {
                let off = start + rel;
                hits.push(CryptConstantHit {
                    va: block.va + off as u64,
                    algorithm: pat.algorithm.into(),
                    constant: pat.constant.into(),
                    size: pat.bytes.len(),
                });
                start = off + 1;
                if hits.len() >= 10_000 {
                    return hits;
                }
            }
        }
    }
    hits
}

fn find_subslice(hay: &[u8], needle: &[u8]) -> Option<usize> {
    if needle.is_empty() || hay.len() < needle.len() {
        return None;
    }
    hay.windows(needle.len()).position(|w| w == needle)
}

pub fn run(prog: &mut Program) -> Result<AnalyzerOutput> {
    let hits = scan_crypt_constants(prog);
    let n = hits.len();
    prog.analysis.crypt_constants = hits.clone();
    // Annotate symbols, EOL comments, and xrefs from code → constant (seed Phase 2/4).
    for h in &hits {
        let name = format!("CRYPT_{}_{}", h.algorithm, h.constant);
        if !prog
            .analysis
            .symbols
            .iter()
            .any(|s| s.va == h.va && s.name == name)
        {
            prog.analysis.symbols.push(crate::program::SymbolInfo {
                va: h.va,
                name,
                demangled: None,
            });
        }
        let comment = format!("CRYPT {} {} ({} bytes)", h.algorithm, h.constant, h.size);
        prog.edits.set_comment(h.va, CommentKind::Eol, comment);
        for xref in crate::xrefs::xrefs_to(prog, h.va, None) {
            let exists = prog
                .analysis
                .references
                .iter()
                .any(|r| r.from == xref.from && r.to == xref.to && r.kind == "crypt_const");
            if !exists {
                prog.analysis.references.push(ReferenceInfo {
                    from: xref.from,
                    to: xref.to,
                    kind: "crypt_const".into(),
                });
            }
        }
    }
    Ok(AnalyzerOutput {
        name: "Find Crypt".into(),
        status: "ok".into(),
        message: format!("found {n} cryptographic constant hit(s)"),
        crypt_constants: Some(hits),
        ..Default::default()
    })
}

/// Function / site VAs that reference cryptographic constants (decoder seeds).
pub fn crypt_xref_seed_vas(prog: &Program) -> Vec<u64> {
    let mut out: Vec<u64> = prog
        .analysis
        .references
        .iter()
        .filter(|r| r.kind == "crypt_const")
        .map(|r| r.from)
        .collect();
    out.sort_unstable();
    out.dedup();
    out
}

/// Public AES S-box for recipe decrypt.
pub fn aes_sbox() -> &'static [u8; 256] {
    &AES_SBOX
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::program::{MemoryBlock, Program};

    #[test]
    fn finds_aes_sbox_in_block() {
        let mut prog = Program::new("t".into(), "raw");
        let mut bytes = vec![0u8; 32];
        bytes.extend_from_slice(&AES_SBOX);
        prog.blocks.push(MemoryBlock {
            name: ".rdata".into(),
            va: 0x1000,
            size: bytes.len() as u64,
            bytes,
            readable: true,
            writable: false,
            executable: false,
        });
        let hits = scan_crypt_constants(&prog);
        assert!(hits
            .iter()
            .any(|h| h.algorithm == "AES" && h.constant == "SBOX"));
        assert_eq!(
            hits.iter().find(|h| h.constant == "SBOX").unwrap().va,
            0x1020
        );
    }

    #[test]
    fn finds_tea_delta_and_new_algorithm_tables() {
        let mut prog = Program::new("t".into(), "raw");
        let mut bytes = Vec::new();
        bytes.extend_from_slice(&TEA_DELTA);
        bytes.extend_from_slice(&[0u8; 7]);
        bytes.extend_from_slice(&SHA512_K_PREFIX);
        bytes.extend_from_slice(&[0u8; 11]);
        bytes.extend_from_slice(&TWOFISH_Q0_PREFIX);
        bytes.extend_from_slice(&[0u8; 13]);
        bytes.extend_from_slice(&SM4_FK);
        prog.blocks.push(MemoryBlock {
            name: ".rdata".into(),
            va: 0x2000,
            size: bytes.len() as u64,
            bytes,
            readable: true,
            writable: false,
            executable: false,
        });

        let hits = scan_crypt_constants(&prog);
        assert!(hits
            .iter()
            .any(|h| h.algorithm == "TEA" && h.constant == "DELTA"));
        assert!(hits
            .iter()
            .any(|h| h.algorithm == "SHA512" && h.constant == "K_PREFIX"));
        assert!(hits
            .iter()
            .any(|h| h.algorithm == "Twofish" && h.constant == "Q0_PREFIX"));
        assert!(hits
            .iter()
            .any(|h| h.algorithm == "SM4" && h.constant == "FK"));
    }
}
